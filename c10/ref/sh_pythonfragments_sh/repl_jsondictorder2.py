print('>>>', '''import json''')
import json
print('>>>', '''a = json.dumps({'one': 1, 'two': 2})''')
a = json.dumps({'one': 1, 'two': 2})
print('>>>', '''a''')
print(a)
print('>>>', '''b = json.dumps({'two': 2, 'one': 1})''')
b = json.dumps({'two': 2, 'one': 1})
print('>>>', '''b''')
print(b)
print(">>>", """f'{repr(a)} == {repr(b)}: {a == b}'""")
print(f'{repr(a)} == {repr(b)}: {a == b}')
print('>>>', '''A = json.loads(a)''')
A = json.loads(a)
print('>>>', '''B = json.loads(b)''')
B = json.loads(b)
print(">>>", """f'{A} == {B}: {A == B}'""")
print(f'{A} == {B}: {A == B}')
